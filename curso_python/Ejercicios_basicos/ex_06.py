a = int(input("Introduce un numero: "))
b = int(input("Introduce otro numero: "))

res = a + b
print("suma ", a, " + ", b, " = ", res)
res = a - b
print("resta ", a, " - ", b, " = ", res)
res = a * b
print("multiplicacion ", a, " x ", b, " = ", res)
res = a / b
print("division ", a, " / ", b, " = ", res)